int H,W;

void printlife(vector<vector<int>>life,int H,int W){
    setcursol(0,0);
    attribute(attr(BOLD));
    attribute(attr(REVERSE));
    for(int i=0;i<H;i++){
        for(int j=0;j<W;j++){
            charcolor(color(life[i][j]));
            //printf("  ");
            printf(" %d",life[i][j]);
        }
        newline();
    }
    attribute(attr(RESET));
    return;
}

vector<vector<int>> next_generation(vector<vector<int>>life,int H,int W){

    vector<vector<int>>next(H,vector<int>(W));

    const int alive=2,dead=7;
    int d8[10]={1,1,1,0,-1,-1,-1,0,1,1};//0-7 2-9

    for(int i=0;i<H;i++){
        for(int j=0;j<W;j++){
            int alive_cnt=0;
            for(int k=0;k<8;k++){
                int h=i+d8[k],w=j+d8[k+2];
                if(!(0<=h&&h<H&&0<=w&&w<W))continue;
                alive_cnt+=(life[h][w]==alive);
            }
            switch (life[i][j]){
                case alive: next[i][j]=((alive_cnt==2||alive_cnt==3)?alive:dead);break;
                case dead:next[i][j]=(alive_cnt==3?alive:dead);break;
            }
        }
    }
    return next;
}

int main(){
    clearscreen();
    setcursol(0,0);
    cin>>H>>W;
    vector<vector<int>>life(H,vector<int>(W));

    char c;
    for(int i=0;i<H*W;i++){cin>>c;life[i/W][i%W]=c-'0';}

    while(1){
        printlife(life,H,W);
        fflush(stdout);
        usleep(50000);
        life=next_generation(life,H,W);
    }
    return 0;
}



